    void init() {
        this.fillTable();
        this.row = -1;
        this.updateStatus();
    }

    void fillTable() { // Fill data to tblNhanVien
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectAll();
            for (NhanVien nv : list) {
                Object[] row = {nv.getMaNV(), nv.getHoTen(), nv.getMatKhau(), nv.getVaiTro() ? "Trưởng Phòng" : "Nhân Viên", nv.getEmail(), nv.getSdt(), nv.getHinh()};
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Loi truy van du lieu");
        }
    }

    void setForm(NhanVien nv) { // Dsiplay NhanVien to form 
        try {
            txtMaNV.setText(nv.getMaNV());
            txtHoTen.setText(nv.getHoTen());
            txtMatKhau.setText(nv.getMatKhau());
            if (nv.getVaiTro()) {
                rdoTruongPhong.setSelected(nv.getVaiTro());
            }
            rdoNhanVien.setSelected(!nv.getVaiTro());
            txtEmail.setText(nv.getEmail());
            txtSoDienThoai.setText(nv.getSdt());
            pathString = nv.getHinh();
            ImageIcon nvA = new ImageIcon(new ImageIcon(hinhPath + pathString).getImage()
                    .getScaledInstance(lblHinhNV.getWidth(), lblHinhNV.getHeight(), Image.SCALE_DEFAULT));
            lblHinhNV.setIcon(nvA);
        } catch (Exception e) {
            rdoTruongPhong.setSelected(false);
        }
    }

    NhanVien getForm() { // Create new NhanVien from form
        NhanVien nv = new NhanVien();
        boolean isValidated = false;
        if (!XValidations.checkIsEmpty(this, txtMaNV, txtHoTen, txtMatKhau, txtEmail, txtSoDienThoai)) {
            isValidated = XValidations.checkEmailPttern(this, txtEmail) ? XValidations.checkPhoneNumberPttern(this, txtSoDienThoai) : false;
        } else {
            isValidated = false;
        }
        nv.setMaNV(txtMaNV.getText());
        nv.setHoTen(txtHoTen.getText());
        nv.setMatKhau(new String(txtMatKhau.getPassword()));
        nv.setVaiTro(rdoTruongPhong.isSelected());
        nv.setEmail(txtEmail.getText());
        nv.setSdt(txtSoDienThoai.getText());
        return isValidated ? nv : null;
    }

    void clearForm() { // [btnMoi]
        NhanVien nv = new NhanVien();
        this.setForm(nv);
        this.row = -1;
        this.updateStatus();
    }

    void updateStatus() { // Change status of btns
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNhanVien.getRowCount() - 1);
        // status form
        txtMaNV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
//         status backward and forward btn
        btnFirst.setEnabled(edit && !first);
        btnPre.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    void insert() { // [btnThem]
        if (getForm() != null) {
            NhanVien nv = getForm();
            try {
                guiQRCode();
                dao.insert(nv);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Them moi thanh cong");
            } catch (Exception e) {
                MsgBox.alert(this, "Them moi that bai");
                e.printStackTrace();
            }
        }

    }

    void update() { // [btnSua]\
        if (getForm() != null) {
            NhanVien nv = getForm();
            try {
                dao.update(nv);
                this.fillTable();
                MsgBox.alert(this, "Cap nhat thanh cong");
            } catch (Exception e) {
                MsgBox.alert(this, "Cap nhat that bai");
                e.printStackTrace();
            }
        }
    }

    void delete() { // [btnXoa]
        if (!XValidations.checkIsEmpty(this, txtMaNV)) {
            String manv = txtMaNV.getText();
            if (manv.equals(Auth.user.getMaNV())) {
                MsgBox.alert(this, "Ban khong the xoa chinh ban!");
            } else if (MsgBox.confirm(this, "Ban thuc su muon xoa nhan vien nay?")) {
                try {
                    dao.delete(manv);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xoa thanh cong");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xoa that bai");
                    e.printStackTrace();
                }
            }
        }
    }

    void edit() { // [tblNhanVien double click]
        String manv = (String) tblNhanVien.getValueAt(this.row, 0);
        NhanVien nv = dao.selectById(manv);
        this.setForm(nv);
        this.updateStatus();
    }

    private void first() {
        this.row = 0;
        this.edit();
        this.showDetails();
    } // btnFirst

    private void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
        this.showDetails();
    } // btnPrev

    private void next() {
        if (this.row < tblNhanVien.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
        this.showDetails();
    } // btnNext

    private void last() {
        this.row = tblNhanVien.getRowCount() - 1;
        this.edit();
        this.showDetails();
    } // btnLast

    public void showDetails() {
        if (row > -1) {
            tblNhanVien.setRowSelectionInterval(row, row);
        }
    }
